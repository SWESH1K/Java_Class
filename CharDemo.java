public class CharDemo {
    public static void main(String[] args) {
        char ch1;

        ch1 = 'X'; // Initalizing ch1 with X.

        System.out.println("Original Ch1 value = " + ch1);

        ch1++; // Incrementing ch1 value by 1. X -> Y.

        System.out.println("The value of Ch1 after incrementing = " + ch1);
    }
}

